// Helper to find location by IDs
export const findDivision = (divisionId: string) => divisions.find(d => d.id === divisionId);
export const findZilla = (divisionId: string, zillaId: string) =>
  findDivision(divisionId)?.zillas.find(z => z.id === zillaId);
export const findUpazila = (divisionId: string, zillaId: string, upazilaId: string) =>
  findZilla(divisionId, zillaId)?.upazilas.find(u => u.id === upazilaId);

export const getCoordinates = (divisionId: string, zillaId?: string, upazilaId?: string): { lat: number; lng: number } | null => {
  if (upazilaId && zillaId) {
    const upazila = findUpazila(divisionId, zillaId, upazilaId);
    if (upazila) return { lat: upazila.lat, lng: upazila.lng };
  }
  if (zillaId) {
    const zilla = findZilla(divisionId, zillaId);
    if (zilla) return { lat: zilla.lat, lng: zilla.lng };
  }
  const division = findDivision(divisionId);
  if (division && division.zillas.length > 0) {
    return { lat: division.zillas[0].lat, lng: division.zillas[0].lng };
  }
  return null;
};

// Find nearest upazila from GPS coordinates
export const findNearestLocation = (lat: number, lng: number): { division: string; zilla: string; upazila: string } => {
  let best = { division: 'dhaka', zilla: 'dhaka', upazila: 'savar' };
  let bestDist = Infinity;
  for (const div of divisions) {
    for (const zilla of div.zillas) {
      for (const upazila of zilla.upazilas) {
        const dLat = lat - upazila.lat;
        const dLng = lng - upazila.lng;
        const dist = dLat * dLat + dLng * dLng;
        if (dist < bestDist) {
          bestDist = dist;
          best = { division: div.id, zilla: zilla.id, upazila: upazila.id };
        }
      }
    }
  }
  return best;
};
